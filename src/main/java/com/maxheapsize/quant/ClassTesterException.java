package com.maxheapsize.quant;

public class ClassTesterException extends RuntimeException {

  public ClassTesterException() {
    super();
  }

  public ClassTesterException(String message) {
    super(message);
  }

  public ClassTesterException(String message, Throwable cause) {
    super(message, cause);
  }

  public ClassTesterException(Throwable cause) {
    super(cause);
  }
}
